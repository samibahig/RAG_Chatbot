import os
import streamlit as st
from langchain.llms import OpenAI
from langchain.chains import RetrievalQA
from langchain.embeddings.openai import OpenAIEmbeddings
from langchain.vectorstores import Chroma
from langchain.text_splitter import RecursiveCharacterTextSplitter
from langchain.document_loaders import TextLoader

# ============================================================
# 1. PAGE CONFIG
# ============================================================
st.set_page_config(
    page_title="RAG Support Chatbot",
    page_icon="💬",
    layout="centered"
)

st.title("💬 RAG Customer Support Chatbot")
st.caption("Powered by LangChain · OpenAI GPT-4 · ChromaDB")

# ============================================================
# 2. LOAD & INDEX DOCUMENTS (cached so it only runs once)
# ============================================================
@st.cache_resource
def load_rag_chain():
    # --- Load documents from the data/ folder ---
    loader = TextLoader("data/support_doc.txt", encoding="utf-8")
    docs = loader.load()

    # --- Split into chunks ---
    splitter = RecursiveCharacterTextSplitter(
        chunk_size=500,
        chunk_overlap=50
    )
    split_docs = splitter.split_documents(docs)

    # --- Create embeddings ---
    embeddings = OpenAIEmbeddings(model="text-embedding-3-small")

    # --- Store in ChromaDB vector store ---
    vectorstore = Chroma.from_documents(
        split_docs,
        embeddings,
        collection_name="support_docs"
    )

    # --- Build retriever (top 3 most relevant chunks) ---
    retriever = vectorstore.as_retriever(
        search_type="similarity",
        search_kwargs={"k": 3}
    )

    # --- Initialize LLM ---
    llm = OpenAI(
        model_name="gpt-4",
        temperature=0  # deterministic = more factual
    )

    # --- Create RAG chain ---
    rag_chain = RetrievalQA.from_chain_type(
        llm=llm,
        retriever=retriever,
        return_source_documents=True
    )

    return rag_chain

# ============================================================
# 3. CHAT INTERFACE
# ============================================================

# Check for API key
if "OPENAI_API_KEY" not in os.environ:
    st.warning("⚠️ Please set your OPENAI_API_KEY environment variable.")
    st.stop()

# Load the RAG chain (cached)
rag_chain = load_rag_chain()

# Session state for chat history
if "messages" not in st.session_state:
    st.session_state.messages = []

# Display chat history
for msg in st.session_state.messages:
    with st.chat_message(msg["role"]):
        st.markdown(msg["content"])

# User input
user_input = st.chat_input("Ask a support question...")

if user_input:
    # Show user message
    st.session_state.messages.append({"role": "user", "content": user_input})
    with st.chat_message("user"):
        st.markdown(user_input)

    # Get RAG response
    with st.chat_message("assistant"):
        with st.spinner("Searching documents..."):
            response = rag_chain({"query": user_input})
            answer = response["result"]
            sources = response["source_documents"]

        st.markdown(answer)

        # Show sources in an expander
        with st.expander("📄 Sources used"):
            for i, doc in enumerate(sources, 1):
                source_name = doc.metadata.get("source", "unknown")
                st.markdown(f"**Source {i}:** `{source_name}`")
                st.caption(doc.page_content[:300] + "...")

    st.session_state.messages.append({"role": "assistant", "content": answer})


